package com.samanyu.locationinfosender;

public class ContactInformation {
    private String mName = new String();
    private String mPhoneNumber = new String();
    private String mEmailId = new String();
    private String mPostalAddress = new String();

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmPhoneNumber() {
        return mPhoneNumber;
    }

    public void setmPhoneNumber(String mPhoneNumber) {
        this.mPhoneNumber = mPhoneNumber;
    }

    public String getmEmailId() {
        return mEmailId;
    }

    public void setmEmailId(String mEmailId) {
        this.mEmailId = mEmailId;
    }

    public String getmPostalAddress() {
        return mPostalAddress;
    }

    public void setmPostalAddress(String mPostalAddress) {
        this.mPostalAddress = mPostalAddress;
    }
}
